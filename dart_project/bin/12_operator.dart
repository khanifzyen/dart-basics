void main() {
  // Operator Aritmatika
  int c = 6;
  c = c + 6;
  print(c); // Hasilnya 12

  // Operator Relasional
  String kota = 'Jepara';
  if (kota == 'Jepara')
    true;
  else
    false;
  print(kota); // Hasilnya Jepara

  // Operator Logika
  var A = true;
  var B = false;
  var expr = A && B;
  print(expr); // Hasilnya false
}
